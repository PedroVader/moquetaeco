// Helper function para obtener FAQs por tipo
export function getFaqsByTipo(tipo: string): FAQ[] {
  const faqsMap: Record<string, FAQ[]> = {
    general: faqsGenerales,
    eventos: [...faqsGenerales.slice(0, 4), ...faqsEventos],
    ferias: [...faqsGenerales.slice(0, 4), ...faqsFerias],
    stands: [...faqsGenerales.slice(0, 4), ...faqsStands],
    bodas: [...faqsGenerales.slice(0, 4), ...faqsBodas],
    congresos: [...faqsGenerales.slice(0, 4), ...faqsCongresos],
  };
  return faqsMap[tipo] || faqsGenerales;
}

